public class VariableAssignment {
	public static void main(String argv[]) {
		int score;
		score = 7;
		System.out.println(score);
		
		double price = 302.25;
		System.out.println(price);
		
		boolean won = false;
		System.out.println(won);
		won = true;
		System.out.println(won);
		
		String name = "Bob";
		System.out.println(name);
	}
}